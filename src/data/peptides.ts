export const getPeptideById = (id: string): Peptide | undefined =>
  peptides.find((p) => p.id === id);

export const getPeptidesByCategory = (categoryId: string): Peptide[] =>
  peptides.filter((p) => p.categoryId === categoryId);

export const getCategoryById = (id: string): Category | undefined =>
  categories.find((c) => c.id === id);

export const searchPeptides = (query: string): Peptide[] => {
  const q = query.toLowerCase();
  return peptides.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.shortDescription.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.tags?.some((t) => t.toLowerCase().includes(q)) ||
      p.benefits?.some((b) => b.toLowerCase().includes(q))
  );
};
